    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String productCode = jTextField1.getText();
        String productName = jTextField2.getText();
        String description = jTextField7.getText();
        int quantity = Integer.parseInt(jTextField3.getText());
        double costPrice = Double.parseDouble(jTextField4.getText());
        double sellingPrice = Double.parseDouble(jTextField5.getText());
        String brand = jTextField6.getText();
        String compatiblePrinter = jTextField8.getText();
        String remark = jTextField9.getText();
        
        PreparedStatement ps;
        String query = "INSERT INTO `products`(`ProductCode`, `ProductName`, `Description`, `Quantity`, `CostPrice`, `SellingPrice`, `Brand`, `Photo`, `CompatiblePrinters`, `Remarks`) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        
        if (productCode.isEmpty()||productName.isEmpty()||brand.isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            try {
                ps = MyConnection.getConnection().prepareStatement(query);

                ps.setString(1, productCode);
                ps.setString(2, productName);
                ps.setString(3, description);
                ps.setInt(4, quantity);
                ps.setDouble(5, costPrice);
                ps.setDouble(6, sellingPrice);
                ps.setString(7, brand);
                InputStream img = new FileInputStream(imgpath);
                ps.setBlob(8, img);
                ps.setString(9, compatiblePrinter);
                ps.setString(10, remark);

                if (ps.executeUpdate() > 0){
                    displayTable();
                    ClearText();
                    JOptionPane.showMessageDialog(null, "New Product was added successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Product was not added!", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            catch (SQLException ex) {
                Logger.getLogger(StaffsRegister.class.getName()).log(Level.SEVERE, null, ex);
            }
            catch (FileNotFoundException ex) {
                Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

